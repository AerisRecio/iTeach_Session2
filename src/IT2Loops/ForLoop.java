package IT2Loops;

public class ForLoop {
    public static void main(String[] args) {
        //For Loop
        //When number of repiririons is known.
        // three parts: initializer, condition, increment/decrement
        // prevents infinite loops
        // example printing numbers from 1-10
        for (/*initialize*/ int i = 10; /*condition*/ i>= 1; /*Counter*/ i --){
            System.out.println(i);
        }
    }
}
